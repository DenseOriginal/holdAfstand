class _MyHomePageState extends State<MyHomePage> {
  void sendSingleNotification(String msg) async {
    var androidPlatformChannelSpecifics = AndroidNotificationDetails(
        '2', 'hold afstand', 'Husk at holde afstand',
        importance: Importance.Max, priority: Priority.High, ticker: 'ticker');
    var iOSPlatformChannelSpecifics = IOSNotificationDetails();
    var platformChannelSpecifics = NotificationDetails(
        androidPlatformChannelSpecifics, iOSPlatformChannelSpecifics);
    await flutterLocalNotificationsPlugin.show(
        1, 'Hold afstand', msg, platformChannelSpecifics,
        payload: 'item x');
  }

  void startNotifications() async {
    // Show a notification every minute with the first appearance happening a minute after invoking the method
    var androidPlatformChannelSpecifics = AndroidNotificationDetails(
        '1', 'hold afstand', 'Husk at holde afstand');
    var iOSPlatformChannelSpecifics = IOSNotificationDetails();
    var platformChannelSpecifics = NotificationDetails(
        androidPlatformChannelSpecifics, iOSPlatformChannelSpecifics);
    await flutterLocalNotificationsPlugin.periodicallyShow(
        0,
        'Hold afstand',
        'Husk at holde afstand',
        RepeatInterval.EveryMinute,
        platformChannelSpecifics);

    sendSingleNotification('Distance reminders have been started');
  }

  void stopNotifications() async {
    await flutterLocalNotificationsPlugin.cancelAll();
    sendSingleNotification('Distance reminders have been stopped');
  }

